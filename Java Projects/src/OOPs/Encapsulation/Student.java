package OOPs.Encapsulation;

public class Student {
    private  String name;
    private int  roll_no;
    private int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setRoll_no(int roll_no) {
        this.roll_no = roll_no;
    }

    public void setAge(int age) {
        this.age = age;
    }

  public String getName() {
        return name;
    }

    public int getRoll_no() {
        return roll_no;
    }

    public int getAge() {
        return age;
    }
}









